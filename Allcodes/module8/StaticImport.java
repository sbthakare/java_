package module8;
import static java.lang.Math.*;    // import package
public class StaticImport {

	public static void main(String[] args) {
		System.out.println(pow(3,4));

		System.out.println(abs(-39));
		
		System.out.println(floor(34.67));
		System.out.println(ceil(34.67));
		
		System.out.println(min(30,40));
		System.out.println(max(30,40));
		
		System.out.println(random());
		
		System.out.println(round(22.8));
		
		System.out.println(sqrt(64));
		
	}

}
